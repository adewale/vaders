#!/usr/bin/env node
// Writes buildInfo.ts into web/src/ and worker/src/ with the current git
// commit, root package version, and UTC build timestamp. Run as a pre-build
// hook for web and a pre-deploy hook for the worker so every deployed bundle
// is self-identifying — visible via the `/health` endpoint, on the launch
// screen footer, and in structured startup logs.
//
// Run directly: `node scripts/write-build-info.mjs`
// The commit hash falls back to "dev" when git is unavailable (e.g. a source
// tarball with no .git). A "-dirty" suffix is appended when the working tree
// has uncommitted changes, so deploys made from an unclean checkout are
// obvious in logs and the UI.

import { execSync } from 'node:child_process'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const root = join(here, '..')

function sh(cmd) {
  try {
    return execSync(cmd, { cwd: root, stdio: ['ignore', 'pipe', 'ignore'] })
      .toString()
      .trim()
  } catch {
    return ''
  }
}

const rawHash = sh('git rev-parse --short=12 HEAD') || 'dev'
const dirty = sh('git status --porcelain').length > 0
const commitHash = dirty ? `${rawHash}-dirty` : rawHash
const version = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8')).version

// Idempotency guard.
//
// When wrangler.jsonc's `build.command` runs this script, wrangler's file
// watcher sees the resulting buildInfo.ts change and re-triggers the
// build — which runs this script again — an infinite loop. Fixed by
// making the script a no-op when the *substantive* content is unchanged.
//
// "Substantive" = {version, commitHash}. The buildTime would churn on
// every call (it's `new Date().toISOString()`), so we deliberately
// preserve the existing file's buildTime when (version, commitHash)
// already match. Only a new commit, `-dirty` transition, or version
// bump bumps the timestamp.
function currentBuildTime(existing, newVersion, newCommitHash) {
  if (!existing) return new Date().toISOString()
  const versionMatch = existing.match(/version:\s*"([^"]+)"/)?.[1]
  const commitMatch = existing.match(/commitHash:\s*"([^"]+)"/)?.[1]
  const timeMatch = existing.match(/buildTime:\s*"([^"]+)"/)?.[1]
  if (versionMatch === newVersion && commitMatch === newCommitHash && timeMatch) {
    return timeMatch // preserve — nothing substantive changed
  }
  return new Date().toISOString()
}

const targets = ['web/src/buildInfo.ts', 'worker/src/buildInfo.ts']
for (const rel of targets) {
  const path = join(root, rel)
  const existing = existsSync(path) ? readFileSync(path, 'utf8') : ''
  const buildTime = currentBuildTime(existing, version, commitHash)

  const body = `// AUTO-GENERATED by scripts/write-build-info.mjs — DO NOT EDIT BY HAND.
// Regenerated on every vite build (web) and wrangler deploy (worker) so each
// deployed artifact carries a unique identifier surfaced via /health, the
// launch-screen footer, and structured startup logs.
export const BUILD_INFO = {
  version: ${JSON.stringify(version)},
  commitHash: ${JSON.stringify(commitHash)},
  buildTime: ${JSON.stringify(buildTime)},
} as const

export type BuildInfo = typeof BUILD_INFO
`

  // Skip the write entirely if the file content is byte-identical. That
  // keeps mtime stable for tools (wrangler, vite) that re-trigger on
  // file-watch events.
  if (existing === body) {
    console.log(`unchanged ${rel}  version=${version}  commit=${commitHash}`)
    continue
  }
  writeFileSync(path, body)
  console.log(`wrote ${rel}  version=${version}  commit=${commitHash}`)
}
