#!/usr/bin/env node
// Writes buildInfo.ts into web/src/ and worker/src/ with the current git
// commit, root package version, and UTC build timestamp. Run as a pre-build
// hook for web and a pre-deploy hook for the worker so every deployed bundle
// is self-identifying — visible via the `/health` endpoint, on the launch
// screen footer, and in structured startup logs.
//
// Run directly: `node scripts/write-build-info.mjs`
// The commit hash falls back to "dev" when git is unavailable (e.g. a source
// tarball with no .git). A "-dirty" suffix is appended when the working tree
// has uncommitted changes, so deploys made from an unclean checkout are
// obvious in logs and the UI.

import { execSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const root = join(here, '..')

function sh(cmd) {
  try {
    return execSync(cmd, { cwd: root, stdio: ['ignore', 'pipe', 'ignore'] })
      .toString()
      .trim()
  } catch {
    return ''
  }
}

const rawHash = sh('git rev-parse --short=12 HEAD') || 'dev'
const dirty = sh('git status --porcelain').length > 0
const commitHash = dirty ? `${rawHash}-dirty` : rawHash
const version = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8')).version
const buildTime = new Date().toISOString()

const body = `// AUTO-GENERATED by scripts/write-build-info.mjs — DO NOT EDIT BY HAND.
// Regenerated on every vite build (web) and wrangler deploy (worker) so each
// deployed artifact carries a unique identifier surfaced via /health, the
// launch-screen footer, and structured startup logs.
export const BUILD_INFO = {
  version: ${JSON.stringify(version)},
  commitHash: ${JSON.stringify(commitHash)},
  buildTime: ${JSON.stringify(buildTime)},
} as const

export type BuildInfo = typeof BUILD_INFO
`

const targets = ['web/src/buildInfo.ts', 'worker/src/buildInfo.ts']
for (const rel of targets) {
  writeFileSync(join(root, rel), body)
  console.log(`wrote ${rel}  version=${version}  commit=${commitHash}`)
}
